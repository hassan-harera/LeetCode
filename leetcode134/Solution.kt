package com.harera.leetcode.leetcode134

class Solution {
    fun canCompleteCircuit(gas: IntArray, cost: IntArray): Int {

        val tankList = mutableListOf<Pair<Int, Int>>()
        gas.forEachIndexed { index, stationGas ->
            tankList.add(Pair(stationGas - cost[index], index))
        }
        tankList.sortByDescending { it.first }

        tankList.forEach {
            var tankGas = 0
            for (idx in it.second..gas.lastIndex) {
                if (tankGas < 0) {
                    break
                }
                tankGas += gas[idx] - cost[idx]
            }
            for (idx in 0 until it.second) {
                if (tankGas < 0) {
                    break
                }
                tankGas += gas[idx] - cost[idx]
            }
            if (tankGas >= 0) {
                return it.second
            }
        }
        return -1
    }
}


